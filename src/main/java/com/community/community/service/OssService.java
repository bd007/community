        InputStream input = new FileInputStream(toFile);

        // 上传文件。
        ossClient.putObject(bucketName, "test/" + file.getOriginalFilename(), input);
        OSSObject object = ossClient.getObject(bucketName, "test/" + file.getOriginalFilename());
        ossClient.setObjectAcl(bucketName, "test/" + file.getOriginalFilename(), CannedAccessControlList.PublicRead);
        // 关闭OSSClient。
        ossClient.shutdown();

        FileDTO fileDTO = new FileDTO();
        fileDTO.setSuccess(1);
        fileDTO.setMessage("成功");
        fileDTO.setUrl(object.getResponse().getUri());
        return fileDTO;
