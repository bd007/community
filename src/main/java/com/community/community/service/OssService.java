        MultipartHttpServletRequest multipartRequest = (StandardMultipartHttpServletRequest) request;
        MultipartFile file = multipartRequest.getFile("editormd-image-file");
        //File fileLocation = ((ApplicationPart) ((StandardMultipartHttpServletRequest.StandardMultipartFile) file).part).location;
        //PutObjectRequest putObjectRequest = new PutObjectRequest(bucketName, firstKey, new File(file.getOriginalFilename()));

